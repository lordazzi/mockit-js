export class Calc {
    public sum(a: number, b: number) {
        return a + b;
    }

    public subtract(a: number, b: number) {
        return a - b;
    }

    public divide(a: number, b: number) {
        return a / b;
    }

    public multiply(a: number, b: number) {
        return a * b;
    }
}
